/* Tipos de datos */

// Number
var number1 = 42; /* Entero */
var number2 = 19.9; /* Decimal */
console.log(number1);
console.log(number2);

// String
var text = "Hola, me llamo Manz"; /* Comillas dobles */
let mensaje = 'Hola, ¿cómo estás?'; /* Comillas simples */
console.log(mensaje);
console.log(text);

// Boolean
var boolean1 = true; /* Verdadero */
var boolean2 = false; /* Falso */
console.log(boolean1);
console.log(boolean2);

// Null
let resultado = null;   //null
console.log(resultado);

// Undefined
var notDefined;   // undefined
console.log(notDefined)

// Symbol
let id = Symbol("identificador");
console.log(id)

// BigInt
let a = 123n;
let b = 456n;
let suma = a + b; // Resultado: 579n
console.log(suma);
